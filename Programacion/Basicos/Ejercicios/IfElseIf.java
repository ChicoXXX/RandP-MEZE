
// Programa para mostrar el uso de if-else-if
public class IfElseIf
{
    public static void main(String args[])
    {
        int i = 21;

        if (i == 10)
            System.out.println("i es igual a 10");
        else if (i == 15)
            System.out.println("i es igual a 15");
        else if (i == 20)
            System.out.println("i es igual a 20");
        else
            System.out.printf("i no esta evaluado");
    }
}
