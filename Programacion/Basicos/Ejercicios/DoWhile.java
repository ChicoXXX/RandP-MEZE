
// Programa para mostrar el uso de Do-While
public class DoWhile
{
    public static void main(String args[])
    {
        int variable = 0;
        System.out.println(variable);
        do {
            variable = variable + 1;
            System.out.println(variable);
        } while( variable == 1 );
        System.out.println(variable);
    }
}

