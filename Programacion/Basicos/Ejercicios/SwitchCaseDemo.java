
// Ejemplo de uso de switch-case en java

class SwitchCaseDemo
{
    public static void main(String args[])
    {
        int i = 1;
        switch (i)
        {
        case 0:
            System.out.println("i vale cero.");
            break;
        case 1:
            System.out.println("i vale uno.");
            break;
        case 2:
            System.out.println("i vale dos.");
            break;
        default:
            System.out.println("i es mayor que dos.");
        }
    }
}
